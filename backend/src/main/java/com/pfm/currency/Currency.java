package com.pfm.currency;

import com.fasterxml.jackson.annotation.JsonIgnore;
import io.swagger.v3.oas.annotations.media.Schema;
import java.math.BigDecimal;
import javax.persistence.Entity;
import javax.persistence.GeneratedValue;
import javax.persistence.GenerationType;
import javax.persistence.Id;
import lombok.AllArgsConstructor;
import lombok.Builder;
import lombok.Data;
import lombok.NoArgsConstructor;

@Data
@NoArgsConstructor
@AllArgsConstructor
@Entity
@Builder
public class Currency {

  @Id
  @GeneratedValue(strategy = GenerationType.IDENTITY)
  @Schema(description = "Currency id (generated by application)", required = true, example = "1")
  private Long id;

  @Schema(description = "Currency name", required = true, example = "EUR")
  private String name;

  @Schema(description = "Currency exchange rate", required = true, example = "4.24")
  private BigDecimal exchangeRate;

  @JsonIgnore
  private Long userId;
}
